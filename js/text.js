function drawText(el, t) {
    if (typeof(el) === "string") el = e(el);
    el.setAttribute('d', getD(t));
    el.parentElement.innerHTML += " ";
}

function getD(t) {
    let d = "";
    let prev = "";
    for (let c of t.split("")) {
        d += kern[prev + c] ?? "";
        d += path[c];
        prev = c;
    }
    return d;
}

